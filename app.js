function questionType(prompt) {
  const p = String(prompt || "");
  // EJU-style skill buckets (8~10)
  if (p.includes("内容と合致") || p.includes("本文") && p.includes("合致")) return "내용 일치";
  if (p.includes("筆者") && (p.includes("主張") || p.includes("意図") || p.includes("最も"))) return "필자 주장/의도";
  if (p.includes("態度") || p.includes("主体") || p.includes("立場") || p.includes("評価")) return "태도/관점";
  if (p.includes("要旨") || p.includes("まとめ") || p.includes("最も適切") && p.includes("要約")) return "요지/요약";
  if (p.includes("語") || p.includes("語句") || p.includes("表現") || p.includes("言い換え")) return "어휘/표현";
  if (p.includes("推測") || p.includes("意味") && p.includes("考え") || p.includes("どのような") && p.includes("意味")) return "문맥 추론";
  if (p.includes("理由") || p.includes("根拠") || p.includes("なぜ") || p.includes("因果")) return "근거/이유";
  if (p.includes("接続") || p.includes("論理") || p.includes("しかし") || p.includes("つまり")) return "논리/접속";
  if (p.includes("空欄") || p.includes("入る") || p.includes("挿入") || p.includes("入れる")) return "빈칸/삽입";
  return "기타";
}

