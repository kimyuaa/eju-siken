function koObjParticle(word) {
  const w = String(word || "").trim();
  if (!w) return "을/를";
  const last = w[w.length - 1];
  const code = last.charCodeAt(0);
  // Hangul syllables range
  if (code < 0xac00 || code > 0xd7a3) return "을/를";
  const hasJong = ((code - 0xac00) % 28) !== 0;
  return hasJong ? "을" : "를";
}

function koSubjParticle(word) {
  const w = String(word || "").trim();
  if (!w) return "이/가";
  const last = w[w.length - 1];
  const code = last.charCodeAt(0);
  if (code < 0xac00 || code > 0xd7a3) return "이/가";
  const hasJong = ((code - 0xac00) % 28) !== 0;
  return hasJong ? "이" : "가";
}

function cleanKoMeaning(meaningKo) {
  const m = String(meaningKo || "").trim();
  if (!m) return "";
  // prefer first chunk before commas/parentheses
  return m.split(/[,(]/)[0].trim();
}

