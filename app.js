function gradeAndRender() {
  // allow re-select + re-grade
  clearFeedback();
  graded = true;

  const p = currentPassage();
  const answers = getAnswers();
  const ts = Date.now();
  let correctCount = 0;
  /** @type {Array<{ ts: number, passageId: string, passageIdx: number, qId: string, qPrompt: string, picked: string, answer: string }>} */
  const mistakes = [];

  p.questions.forEach((q) => {
    const picked = answers[q.id];
    const ok = picked === q.answerIndex;
    if (ok) correctCount += 1;
    const qEl = document.querySelector(`.q[data-qid="${q.id}"]`);
    if (!qEl) return;

    qEl.classList.add(ok ? "q--ok" : "q--bad");

    const badge = document.createElement("div");
    badge.className = `qBadge ${ok ? "qBadge--ok" : "qBadge--bad"}`;
    badge.textContent = ok ? "정답" : "오답";

    const head = qEl.querySelector(".q__head");
    if (head) head.appendChild(badge);

    const pickedText =
      typeof picked === "number" ? `${picked + 1}. ${q.choices[picked]}` : "(미응답)";
    const answerText = `${q.answerIndex + 1}. ${q.choices[q.answerIndex]}`;

    if (!ok) {
      mistakes.push({
        ts,
        passageId: p.id,
        passageIdx: currentPassageIdx,
        qId: q.id,
        qPrompt: `Q${p.questions.findIndex((qq) => qq.id === q.id) + 1}. ${q.prompt}`,
        picked: pickedText,
        answer: answerText,
      });
    }

    const fb = document.createElement("div");
    fb.className = "feedback";
    const koReason = q.explanationKo ? `<div><span class="paper__label">이유(한국어)</span> ${escapeHtml(q.explanationKo)}</div>` : "";
    fb.innerHTML = `
      <div><span class="paper__label">내 답</span> <strong>${escapeHtml(pickedText)}</strong></div>
      <div><span class="paper__label">정답</span> <strong>${escapeHtml(answerText)}</strong></div>
      <div><span class="paper__label">해설(일본어)</span> ${escapeHtml(q.explanation || "-")}</div>
      ${koReason}
    `;
    qEl.appendChild(fb);
  });

  renderPassage(); // show translation under passage

  // persist per-passage result for final analysis
  const qResults = p.questions.map((q, idx) => {
    const picked = answers[q.id];
    const ok = picked === q.answerIndex;
    return { idx: idx + 1, qId: q.id, type: questionType(q.prompt), ok, picked };
  });
  saveReadingResult(p.id, {
    passageIdx: currentPassageIdx,
    label: p.label,
    title: p.title,
    topic: passageTopic(p),
    ts,
    correct: correctCount,
    total: p.questions.length,
    qResults,
  });
  renderOverallProgress();
  renderMarkCard();

  recordAttempt({
    ts,
    passageId: p.id,
    passageIdx: currentPassageIdx,
    correct: correctCount,
    total: p.questions.length,
    mistakes,
  });

  const isLast = currentPassageIdx >= EXAM.passages.length - 1;
  const nextBtn = document.querySelector("#btnNext");
  const gradeBtn = document.querySelector("#btnGrade");
  const retryBtn = document.querySelector("#btnRetry");
  const finishBtn = document.querySelector("#btnFinish");
  if (gradeBtn) gradeBtn.textContent = "해설";
  if (retryBtn) retryBtn.hidden = false;
  if (nextBtn) nextBtn.disabled = isLast;
  if (finishBtn) finishBtn.hidden = !isLast;
}

