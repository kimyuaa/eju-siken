function generateQuizSet(vocab, count = 15) {
  const pool = vocab.filter((v) => hasKanji(v.term) && v.reading && v.meaningKo);
  if (pool.length < count) return null;
  const shuffled = pool.slice();
  for (let i = shuffled.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  const chosen = shuffled.slice(0, count);

  const readingPool = unique(pool.map((v) => v.reading).filter(Boolean));
  const meaningPool = unique(pool.map((v) => v.meaningKo).filter(Boolean));

  return chosen.map((v, idx) => {
    const readingChoices = buildChoices(v.reading, readingPool, 4);
    const meaningChoices = buildChoices(v.meaningKo, meaningPool, 4);
    return {
      id: `q${idx + 1}`,
      term: v.term,
      tag: v.tag,
      reading: v.reading,
      meaningKo: v.meaningKo,
      readingChoices,
      readingAnswerIndex: readingChoices.indexOf(v.reading),
      meaningChoices,
      meaningAnswerIndex: meaningChoices.indexOf(v.meaningKo),
    };
  });
}

