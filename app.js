function renderOverallProgress() {
  const el = document.querySelector("#overallProgress");
  if (!el) return;
  const total = totalQuestionCount();

  // Prefer draft answers (채점 전 선택 포함) so progress doesn't get stuck.
  const d = getReadingDraft();
  const r = getReadingResults();

  let done = 0;
  EXAM.passages.forEach((p) => {
    const draftAns = d.byPassageId?.[p.id]?.answers;
    if (draftAns && typeof draftAns === "object") {
      p.questions.forEach((q) => {
        if (typeof draftAns[q.id] === "number") done += 1;
      });
      return;
    }

    const saved = r.byPassageId?.[p.id];
    if (saved && Array.isArray(saved.qResults)) {
      saved.qResults.forEach((qr) => {
        if (typeof qr.picked === "number") done += 1;
      });
    }
  });

  el.textContent = `${Math.min(total, done)} / ${total}`;
}

function buildFlatQuestions() {
  /** @type {Array<{ globalIdx: number, passageIdx: number, qId: string, localNo: number }>} */
  const out = [];
  let k = 0;
  EXAM.passages.forEach((p, pIdx) => {
    (p.questions || []).forEach((q, qIdx) => {
      out.push({ globalIdx: k + 1, passageIdx: pIdx, qId: q.id, localNo: qIdx + 1 });
      k += 1;
    });
  });
  return out;
}

function getAnswerStatusMap() {
  const d = getReadingDraft();
  const r = getReadingResults();
  /** @type {Record<string, { picked: number | null, ok: boolean | null }>} */
  const byQid = {};

  // from graded results (includes ok)
  Object.values(r.byPassageId || {}).forEach((p) => {
    (p.qResults || []).forEach((qr) => {
      byQid[String(qr.qId)] = {
        picked: typeof qr.picked === "number" ? qr.picked : null,
        ok: typeof qr.ok === "boolean" ? qr.ok : null,
      };
    });
  });

  // overlay draft picks for unanswered/ungraded
  Object.entries(d.byPassageId || {}).forEach(([pid, v]) => {
    const p = EXAM.passages.find((x) => String(x.id) === String(pid));
    if (!p) return;
    const ans = v?.answers;
    if (!ans || typeof ans !== "object") return;
    (p.questions || []).forEach((q) => {
      const picked = ans[q.id];
      if (typeof picked !== "number") return;
      const prev = byQid[String(q.id)];
      if (prev && typeof prev.ok === "boolean") return; // keep graded ok/bad
      byQid[String(q.id)] = { picked, ok: null };
    });
  });

  return byQid;
}

