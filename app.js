function getStats() {
  const base = {
    attempts: [], // { ts, passageId, passageIdx, correct, total }
    mistakes: [], // { ts, passageId, passageIdx, qId, qPrompt, picked, answer }
    days: {}, // { [yyyy-mm-dd]: { attempts: number } }
  };
  const s = readJson(STORAGE.stats, base);
  if (!s || typeof s !== "object") return base;
  if (!Array.isArray(s.attempts)) s.attempts = [];
  if (!Array.isArray(s.mistakes)) s.mistakes = [];
  if (!s.days || typeof s.days !== "object") s.days = {};
  return s;
}

function clearStats() {
  localStorage.removeItem(STORAGE.stats);
}

function isLoggedIn() {
  return localStorage.getItem(STORAGE.loggedIn) === "true";
}

function recordAttempt({ ts, passageId, passageIdx, correct, total, mistakes }) {
  if (!isLoggedIn()) return; // 기록은 로그인 상태에서만
  const s = getStats();
  s.attempts.unshift({ ts, passageId, passageIdx, correct, total });
  const day = todayKey(ts);
  if (!s.days[day]) s.days[day] = { attempts: 0 };
  s.days[day].attempts += 1;

  if (Array.isArray(mistakes) && mistakes.length) {
    mistakes.forEach((m) => s.mistakes.unshift(m));
  }

  // caps
  s.attempts = s.attempts.slice(0, 400);
  s.mistakes = s.mistakes.slice(0, 120);
  writeJson(STORAGE.stats, s);
}

function inRange(ts, range) {
  if (range === "all") return true;
  const days = Number(range);
  if (!Number.isFinite(days) || days <= 0) return true;
  const now = Date.now();
  return now - ts <= days * 24 * 60 * 60 * 1000;
}

function computeStreak(daysMap) {
  let streak = 0;
  const d = new Date();
  for (;;) {
    const key = todayKey(d.getTime());
    if (daysMap[key]?.attempts) {
      streak += 1;
      d.setDate(d.getDate() - 1);
      continue;
    }
    break;
  }
  return streak;
}

