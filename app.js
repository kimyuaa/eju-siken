function uniqueLinksFromFriendlyBlocks(blocks) {
  const seen = new Set();
  /** @type {Array<{ name: string, platform: string, url: string }>} */
  const out = [];
  blocks.forEach((b) => {
    (b.picks || []).forEach((p) => {
      const key = `${p.url}`;
      if (!p.url || seen.has(key)) return;
      seen.add(key);
      out.push(p);
    });
  });
  return out;
}

