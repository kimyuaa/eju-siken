function pickSampleSentence(text, term) {
  const parts = String(text)
    .replace(/\s+/g, " ")
    .split(/(?<=[。！？!?])\s*/);
  const hit = parts.find((p) => p.includes(term));
  const s = (hit || parts[0] || "").trim();
  if (!s) return "";
  return s.length > 180 ? `${s.slice(0, 180)}…` : s;
}

